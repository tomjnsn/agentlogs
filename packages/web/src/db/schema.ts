let cuidGenerator: (() => string) | undefined;

const getCuidGenerator = () => {
  if (!cuidGenerator) {
    // Lazily initialize to avoid calling crypto APIs at module load (Cloudflare restriction)
    cuidGenerator = init();
  }
  return cuidGenerator;
};

const generateId = () => getCuidGenerator()();

