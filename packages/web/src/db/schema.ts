// BetterAuth relations
export const userRelations = relations(user, ({ many }) => ({
  sessions: many(session),
  accounts: many(account),
  transcripts: many(transcripts),
  teamMemberships: many(teamMembers),
  ownedTeams: many(teams),
}));

export const sessionRelations = relations(session, ({ one }) => ({
  user: one(user, {
    fields: [session.userId],
    references: [user.id],
  }),
}));

export const accountRelations = relations(account, ({ one }) => ({
  user: one(user, {
    fields: [account.userId],
    references: [user.id],
  }),
}));

// AgentLogs relations
export const reposRelations = relations(repos, ({ many }) => ({
  transcripts: many(transcripts),
}));

export const transcriptsRelations = relations(transcripts, ({ one, many }) => ({
  user: one(user, {
    fields: [transcripts.userId],
    references: [user.id],
  }),
  repo: one(repos, {
    fields: [transcripts.repoId],
    references: [repos.id],
  }),
  sharedWithTeam: one(teams, {
    fields: [transcripts.sharedWithTeamId],
    references: [teams.id],
  }),
  transcriptBlobs: many(transcriptBlobs),
  commits: many(commitTracking),
}));

export const commitTrackingRelations = relations(commitTracking, ({ one }) => ({
  user: one(user, {
    fields: [commitTracking.userId],
    references: [user.id],
  }),
  transcript: one(transcripts, {
    fields: [commitTracking.transcriptId],
    references: [transcripts.id],
  }),
}));

// Blob relations
export const blobsRelations = relations(blobs, ({ many }) => ({
  transcriptBlobs: many(transcriptBlobs),
}));

export const transcriptBlobsRelations = relations(transcriptBlobs, ({ one }) => ({
  transcript: one(transcripts, {
    fields: [transcriptBlobs.transcriptId],
    references: [transcripts.id],
  }),
  blob: one(blobs, {
    fields: [transcriptBlobs.sha256],
    references: [blobs.sha256],
  }),
}));

// Team relations
export const teamsRelations = relations(teams, ({ one, many }) => ({
  owner: one(user, {
    fields: [teams.ownerId],
    references: [user.id],
  }),
  members: many(teamMembers),
  invites: many(teamInvites),
  sharedTranscripts: many(transcripts),
}));

export const teamMembersRelations = relations(teamMembers, ({ one }) => ({
  team: one(teams, {
    fields: [teamMembers.teamId],
    references: [teams.id],
  }),
  user: one(user, {
    fields: [teamMembers.userId],
    references: [user.id],
  }),
}));

export const teamInvitesRelations = relations(teamInvites, ({ one }) => ({
  team: one(teams, {
    fields: [teamInvites.teamId],
    references: [teams.id],
  }),
}));
